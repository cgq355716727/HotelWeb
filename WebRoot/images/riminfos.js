function setInfo(id){
	$("[id^='type']").css('background-image','url(http://img1.7daysinn.cn/images/riminfos/tab02.jpg)');
	$("[id='type"+id+"']").css('background-image','url(http://img1.7daysinn.cn/images/riminfos/tab01.jpg)');
	$("[id^='rsInfo']").hide();
	var pidObj=$("[id='rsInfo"+id+"']");
	if(pidObj.html()!=null){
		pidObj.show();
	}
}